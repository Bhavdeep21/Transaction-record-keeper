    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String u=userid.getText();
        String p=new String(passwd.getPassword());
        String np=new String(npasswd.getPassword());
        String rnp=new String(rnpasswd.getPassword());
        msg ms=new msg();
        try
        {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/eroscoin?zeroDateTimeBehavior=convertToNull", "root", "1234");
            Statement stmt=con.createStatement();
            String qq="select * from erosid where user_id='"+u+"'";
            ResultSet rs=stmt.executeQuery(qq);
            int q=0;
            while(rs.next())
            {

                if( p.equals(rs.getString("password")))
                {
                   if(np.equals(rnp))
                   {
                    q=1;
                    String cr="update erosid set password='"+np+"' where user_id='"+u+"'";
                    stmt.executeUpdate(cr);
                    Myinfo mi=new Myinfo();
                    mi.setVisible(true);
                    mi.uid.setText(userid.getText());
                    mi.uid.setVisible(false);
                    ms.setVisible(true);
                    ms.jLabel1.setText("Password Has Been");
                    ms.jLabel2.setText("Changed Successfully");
                    this.dispose();
                   }                   
                   else
                   {
                       ms.setVisible(true);
                       ms.jLabel1.setText("New Password Does Not");
                       ms.jLabel2.setText("Match");
                   }
                }
            }
            if(q==0)
            {
                ms.setVisible(true);
                ms.jLabel1.setText("Wrong");
                ms.jLabel2.setText("Password");
            }

        }
        catch(Exception ex)
        {
           
        }
    }//GEN-LAST:event_jButton2ActionPerformed

