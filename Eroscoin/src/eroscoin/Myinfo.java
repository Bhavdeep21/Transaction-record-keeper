    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String u=uid.getText();
        msg ms=new msg();
        Mrechrecord mr=new Mrechrecord();
        DefaultTableModel tt=(DefaultTableModel) mr.mrr.getModel();
        for(int i=tt.getRowCount() -1; i>=0; i--)
        {
            tt.removeRow(i);
        }
        try
        {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/eroscoin?zeroDateTimeBehavior=convertToNull", "root", "1234");
            Statement stmt=con.createStatement();
            String qq="select * from mrecharge where user_id='"+u+"'";
            ResultSet rs=stmt.executeQuery(qq);
            int q=0;
            while(rs.next())
            {
                tt.addRow(new Object[] {rs.getString("User_ID"), rs.getString("Mobile_no"), rs.getString("Operator"), rs.getString("Payment_Option"), rs.getString("Card_no"), rs.getString("Amount")});
                mr.setVisible(true);
                q=1;
            }
        if(q==0)
        {
            ms.setVisible(true);
            ms.jLabel1.setText("No Record");
            ms.jLabel2.setText("Found");
        }
            
        }
        catch(Exception ex)
        {
            ms.setVisible(true);
            ms.jLabel1.setText(ex.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

