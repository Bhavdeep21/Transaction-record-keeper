String hq=hques.getText();
String ha=hans.getText();
msg ms=new msg();
try
{
    Class.forName("java.sql.Driver");
    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/eroscoin?zeroDateTimeBehavior=convertToNull", "root", "1234");
    Statement stmt=con.createStatement();
    if(p.equals(cp))
    {
         String qq="insert into erosid values('"+un+"','"+ui+"','"+p+"','"+hq+"','"+ha+"')";
        stmt.executeUpdate(qq);
        Home h=new Home();
        h.setVisible(true);
        h.userid.setText(ui);
        h.userid.setVisible(false);
        ms.setVisible(true);
        ms.jLabel1.setText("Your ID has created");
        ms.jLabel2.setText("Succesfully");
        this.setVisible(false);
    }
    else
    {
        ms.setVisible(true);
        ms.jLabel1.setText("Password");
        ms.jLabel2.setText("Doesnot match");
    }
}

catch(Exception ex)
{
    ms.setVisible(true);
    ms.jLabel1.setText(ex.getMessage());
}
