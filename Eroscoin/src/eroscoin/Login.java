    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String u=uid.getText();
String p=new String(passwd.getPassword());
msg ms=new msg();
try
{
    Class.forName("java.sql.Driver");
    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/eroscoin?zeroDateTimeBehavior=convertToNull", "root", "1234");
    Statement stmt=con.createStatement();
    String qq="select * from erosid where user_id='"+u+"'";
    ResultSet rs=stmt.executeQuery(qq);
    int q=0;
    while(rs.next())
    {
       
        if(u.equals(rs.getString("user_id")) && p.equals(rs.getString("password")))
        {
            q=1;
            Home h=new Home();
            h.setVisible(true);
            h.userid.setText(u);
            h.userid.setVisible(false);
            ms.setVisible(true);
            ms.jLabel1.setText("WELCOME TO");
            ms.jLabel2.setText("EROSCOIN");
            this.dispose();
        }
    }
        if(q==0)
        {
            ms.setVisible(true);
            ms.jLabel1.setText("Invalid");
            ms.jLabel2.setText("User Id and Password");           
        }
    
}
catch(Exception ex)
{
    ms.setVisible(true);
    ms.jLabel1.setText(ex.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

